    private void jButtonSizeMemoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSizeMemoryActionPerformed
      // TODO add your handling code here:

      if (jTextFieldSizeMemory.getText().isBlank()) {
        JOptionPane.showMessageDialog(null, "SET THE MEMORY SIZE FIRST.");
      } else {
        String memory = jTextFieldSizeMemory.getText();
        boolean isNumeric = memory.matches("[+]?\\d*(\\.\\d+)?");

        modelo = new DefaultTableModel() {
          @Override
          public boolean isCellEditable(int filas, int columnas) {
            if (columnas == 2) {
              return true;

            } else {
              return false;
            }
          }
        };
        modelo.addColumn("Space");
        modelo.addColumn("Bin Instructions");

        jTable1.setModel(modelo);
        jTable1.getColumnModel().getColumn(0).setMinWidth(60);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(60);
        if (isNumeric) {
          int num = Integer.valueOf(memory);

          for (int i = 0; i < num; i++) {
            modelo.setNumRows(num);
            jTable1.setValueAt(i, i, 0);

          }

        }

      }
      executeButton.setEnabled(true);
      //if (jTextFieldSizeMemory.getText() != "") {

      //}

    }//GEN-LAST:event_jButtonSizeMemoryActionPerformed

