    private void jButtonLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadActionPerformed
      // TODO add your handling code here:

      if (flag == true) {
        jButtonSizeMemory.setEnabled(flag);
        try {
          FileReader fr = new FileReader(file);
          BufferedReader br = new BufferedReader(fr);
          String text = "";
          String line = "";
          ArrayList<String[]> instructionsParser = new ArrayList<>();
          int lineNumber = 0;
          int errCount = 0;
          while (((line = br.readLine()) != null)) {
            String lineFormat = line;
            if (line.contains(",")) {
              instructionsParser.add(line.replace(",", "").split(" "));
            } else {
              String newLine = line + ", x";
              instructionsParser.add(newLine.replace(",", "").split(" "));
            }
            if (instructionFormatter(lineFormat)) {
              text += line + "\n";
            } else {
              JOptionPane.showMessageDialog(null, "SYNTAX ERR AT LINE " + lineNumber + ". STOPPING PROCESS.");
              errCount += 1;
            }
            lineNumber++;
          }

          int instructionsQuantity = instructionsParser.size();
          String[] programInstructions = new String[3 * instructionsQuantity];
          int i = 0;
          for (String[] val : instructionsParser) {
            for (String inst : val) {
              programInstructions[i] = inst;
              i += 1;
            }
          }

          if (errCount > 0) {
            text = "FIX THE ERROS AND LOAD THE PROGRAM AGAIN.";
          }
          this.program = new Program(instructionsQuantity, programInstructions);
          textArea.setText(text);

        } catch (FileNotFoundException ex) {
          Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
          Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
      } else {
        JOptionPane.showMessageDialog(null, "LOAD A FILE FIRST.");
      }

    }//GEN-LAST:event_jButtonLoadActionPerformed

